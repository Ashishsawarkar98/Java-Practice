package day3;

public class AssignmentOperators {

	public static void main(String[] args) {
		
		// AssignmentOperators = += -= *= /= %=
		
		int a =10;
		a+=5; //a+5=10+5=15
		System.out.println(a);
		
		int b =15;
		b-=5; //b-5=15-5=10;
		System.out.println(b);
		
		int c = 20;
		c*=5 ; //c*5 =20*5=100
		System.out.println(c);
		
		
	}

}
